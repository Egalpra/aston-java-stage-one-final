package ru.aston.sort_app.model;

public class Movie {
    private int id;
    private String name;
    private int year;
    private String director;
    private double rate;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public int getYear() {
        return year;
    }

    public String getDirector() {
        return director;
    }

    public double getRate() {
        return rate;
    }
}

